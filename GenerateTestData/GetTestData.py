def get_gpa(score):
    """
  根据成绩计算绩点
  Args:
    score: 成绩，可以是浮点数或字符串
  Returns:
    绩点，浮点数
  """

    if isinstance(score, str):
        # 将字符串成绩转换为浮点数
        if score == "优秀":
            score = 95
        elif score == "良好":
            score = 85
        elif score == "中等":
            score = 75
        elif score == "及格":
            score = 65
        else:
            score = 55

    if score < 60:
        gpa = 0.0
    elif score < 64:
        gpa = 1.0
    elif score < 67:
        gpa = 1.3
    elif score < 70:
        gpa = 1.7
    elif score < 74:
        gpa = 2.0
    elif score < 77:
        gpa = 2.3
    elif score < 80:
        gpa = 2.7
    elif score < 84:
        gpa = 3.0
    elif score < 87:
        gpa = 3.3
    elif score < 90:
        gpa = 3.7
    elif score < 94:
        gpa = 4.0
    else:
        gpa = 4.0

    return gpa


