def extract_grade(stu_id):
    grade = stu_id % 1000000 / 10000
    return int(grade + 2000)


def encrypt(pw):
    md5_hash = hashlib.md5()
    md5_hash.update(str(pw).encode('utf-8'))
    return md5_hash.hexdigest()


